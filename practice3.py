# 練習問題2
# 税率計算プログラムを作成してください。(税率もユーザーに入力してもらいます。)
# ※実行結果
# 商品の合計金額を入力してください。>>>15000
# 税率を入力してください。>>>10
# 15000 円の商品を税率 10 %で計算すると、税込み価格は 16500.0 円です。

total = int(input("商品の合計金額を入力してください。"))
tax = int(input("税率を入力してください。"))
intax =  total + total * (tax / 100)

print(f"{total}円の商品を税率{tax}%で計算すると、税込み価格は{intax}円です。")


