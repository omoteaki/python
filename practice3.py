# 身長と体重を表す2つの変数を使って、ユーザーから受け取った数値をもとにBMIを算出するプログラムを作成してください。
# 入力値の変換にはfloat()を使います。
# ※実行結果
# 身長を入力してください。>>>
# 体重を入力してください。>>>
# 身長は 160.4 cmです。
# 体重は 40.3kg です。
# あなたのBMIは 15.66 です。

tall = float(input("身長を入力してください(cm)>>>"))
weight = float(input("体重を入力してください(kg)>>>"))
bmi = weight / ((tall / 100) * (tall / 100))
print("身長は" , tall, "センチです")
print("体重は", weight, "キロです")
print("あなたのBMIは", bmi, "です")


