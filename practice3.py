# 演習問題★★★
# 整数反転
# キーボードから3桁の数値を受け取り、その整数を反転させて出力してください。
# ※実行結果
# num>>>123
# 入力した値を反転すると321になります。

num = int(input("num>>>"))
# num = 123

# 百のくらいは100で割った商
hun = num // 100
# 一の位は10で割った余り
one = num % 10
# print(hun, one)

# 百の位と一の位を引いた数に反転させた百の位と一の位を作って足す
# num = num - hun * 100 - one + one * 100 + hun
# ↑ちょっとわかりづらい
num = num - hun * 100 - one
num = num + one * 100 + hun

print(f"入力した値を反転すると{num}になります。")
